    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (!campoEhValido(jpnArea)) {
            return;
        }
        double notas[] = new double[4];
        //parteDouble retorna um tipo primitivo double enquanto valueOf retorna um objeto Double
        notas[0] = Double.parseDouble(jtfNota1.getText());
        notas[1] = Double.parseDouble(jtfNota2.getText());
        notas[2] = Double.parseDouble(jtfNota3.getText());
        notas[3] = Double.parseDouble(jtfNota4.getText());
        Aluno aluno = new Aluno(jtfNome.getText(), notas);
        DefaultTableModel dtm = (DefaultTableModel) jtbNotas.getModel();
        jtaAlunos.append(aluno.imprime());
        jtaAlunos.append("\n\n");
        limpaCampos(jpnArea);
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (!campoEhValido(jpnTabela)) {
            return;
        }
        double notas[] = new double[4];
        //parteDouble retorna um tipo primitivo double enquanto valueOf retorna um objeto Double
        notas[0] = Double.parseDouble(jtfTNota1.getText());
        notas[1] = Double.parseDouble(jtfTNota2.getText());
        notas[2] = Double.parseDouble(jtfTNota3.getText());
        notas[3] = Double.parseDouble(jtfTNota4.getText());
        Aluno aluno = new Aluno(jtfTNome.getText(), notas);
        DefaultTableModel dtm = (DefaultTableModel) jtbNotas.getModel();
        dtm.addRow(new Object[]{
            aluno.getNome(),
            aluno.getNotas()[0],
            aluno.getNotas()[1],
            aluno.getNotas()[2],
            aluno.getNotas()[3],
            aluno.calculaMedia(notas),
            aluno.ehAprovado(aluno.calculaMedia(notas)) ? "Aprovado" : "Reprovado"
        });

        limpaCampos(jpnTabela);
    }//GEN-LAST:event_jButton1ActionPerformed

